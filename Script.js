var num1 = parseInt(prompt("Number 1"), 10);
var num2 = parseInt(prompt("Number 2"), 10);
document.writeln('<h3 style="color:red;">Operations</h3>');
document.writeln("Add= " + (num1 + num2) + "<br>");
document.writeln("Sub= " + (num1 - num2) + "<br>");
document.writeln("Mul= " + num1 * num2 + "<br>");
document.writeln("Div= " + num1 / num2 + "<br>");
document.writeln("Mod= " + (num1 % num2) + "<br>");
